class Solution {
public:
    long long countCommas(long long n) {
        if(n < 1000){
            return 0;
        }
        else if(n >= 1000 && n <= 999999){
            return (n - 1000 + 1);
        }
        else if(n >= 1e6 && n <= 999999999){
            return ((n - 1e6 + 1)* 2  + (999999 - 1000 + 1 ) * 1);
        }else if(n >= 1e9 && n <= 999999999999){
            return ((n - 1e9 + 1) * 3 + (999999999 - 1000000 + 1) * 2 + (999999-1000 + 1) * 1);
        }else if(n >= 1e12  && n <= 999999999999999){
            return ((n - 1e12 + 1) * 4 + (999999999999 - 1000000000 + 1) * 3  + (999999999 - 1000000 + 1) * 2 + (999999 - 1000 + 1)*1);
        }

         return ((n - 1e12 + 1) * 4 + (999999999999 - 1000000000 + 1) * 3  + (999999999 - 1000000 + 1) * 2 + (999999 - 1000 + 1)*1) + 1; 




        
    }
};